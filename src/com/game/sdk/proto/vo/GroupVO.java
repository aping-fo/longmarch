package com.game.sdk.proto.vo;

import com.google.common.collect.Lists;

import java.util.List;

/**
 *  家庭群信息
 */
public class GroupVO {
    private String id;
    private String creatorOpenId;
    private String iconUrl;
    private String name;
    private int step;
    private int level;
    private List<GroupMemberVO> groupMembers = Lists.newArrayListWithCapacity(8);

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getCreatorOpenId() {
        return creatorOpenId;
    }

    public void setCreatorOpenId(String creatorOpenId) {
        this.creatorOpenId = creatorOpenId;
    }

    public String getIconUrl() {
        return iconUrl;
    }

    public void setIconUrl(String iconUrl) {
        this.iconUrl = iconUrl;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getStep() {
        return step;
    }

    public void setStep(int step) {
        this.step = step;
    }

    public int getLevel() {
        return level;
    }

    public void setLevel(int level) {
        this.level = level;
    }

    public List<GroupMemberVO> getGroupMembers() {
        return groupMembers;
    }

    public void setGroupMembers(List<GroupMemberVO> groupMembers) {
        this.groupMembers = groupMembers;
    }
}
